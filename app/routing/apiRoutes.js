// ===============================================================================
// LOAD DATA

// ===============================================================================

var surveyData = require("../data/friends");


// ===============================================================================
// ROUTING
// ===============================================================================

module.exports = function(app) {
  // API GET Requests
  // ---------------------------------------------------------------------------

    app.get("/api/friends", function(req, res) {
      res.json(surveyData);
    });

 

  // API POST Requests
  // ---------------------------------------------------------------------------

    app.post("/api/survey", function(req, res) {
      var newFriend = req.body;
      surveyData.push(newFriend);
      res.json(newFriend);

    });

  }


 